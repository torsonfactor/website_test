function WishList(class_name_wishList, class_name_block_size, foreground_image) {
        var x = document.getElementsByClassName(class_name_wishList);
        var y = document.getElementsByClassName(class_name_block_size);
        x[0].style.opacity = "7";
        y[0].style.opacity = "7";
    }

